/**
 * 伙伴招募生成超时共享配置
 *
 * 作用（做什么 / 不做什么）：
 * 1) 做什么：集中维护伙伴招募生成链路的统一超时阈值，供 worker 执行层与伙伴文本模型请求层共同复用。
 * 2) 做什么：把“伙伴招募按 300 秒超时”的业务规则收敛成单一入口，避免不同模块再次出现各自硬编码。
 * 3) 不做什么：不负责创建请求、不负责计时中断，也不处理失败后的退款与消息推送。
 *
 * 输入 / 输出：
 * - 输入：无。
 * - 输出：`PARTNER_RECRUIT_GENERATION_TIMEOUT_MS`，表示伙伴招募生成链路统一超时毫秒数。
 *
 * 数据流 / 状态流：
 * 共享超时常量 -> worker 池任务超时 / 伙伴文本模型请求超时 -> 上层任务链路据此决定终止时机。
 *
 * 复用设计说明：
 * 1) 伙伴招募的 worker 总任务超时与模型请求超时必须保持一致，否则会出现内层仍在等待模型、外层已提前退款失败的状态分叉。
 * 2) 提成共享常量后，runner 与预览生成核心只依赖同一个入口，后续调整超时策略时不需要再跨文件排查多个魔法数字。
 *
 * 关键边界条件与坑点：
 * 1) 这里只覆盖伙伴招募链路，不能误复用到洞府研修、云游或洗髓等其他业务，否则会把不同耗时模型强行绑成同一策略。
 * 2) 超时值单位固定为毫秒，调用方不得再自行换算，避免出现 300 被误当成秒或毫秒的单位漂移。
 */
export const PARTNER_RECRUIT_GENERATION_TIMEOUT_MS = 300_000;
