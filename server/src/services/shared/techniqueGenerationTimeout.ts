/**
 * 洞府研修生成超时共享配置
 *
 * 作用（做什么 / 不做什么）：
 * 1) 做什么：集中维护洞府研修 AI 生成链路的统一超时阈值，供 worker 执行层与模型请求层共同复用。
 * 2) 做什么：把“洞府研修应按 300 秒超时”收敛成单一入口，避免不同层各自硬编码导致规则漂移。
 * 3) 不做什么：不负责实际计时、不创建 AbortController，也不处理超时后的退款或推送。
 *
 * 输入 / 输出：
 * - 输入：无。
 * - 输出：`TECHNIQUE_GENERATION_TIMEOUT_MS`，表示洞府研修生成链路统一超时毫秒数。
 *
 * 数据流 / 状态流：
 * 共享超时常量 -> worker 池任务超时 / 模型请求超时 -> 上层生成链路据此决定中断时机。
 *
 * 复用设计说明：
 * 1) 洞府研修的 worker 总任务超时与模型请求超时必须保持一致，否则会出现内层仍在等待模型、外层已提前判失败的状态分叉。
 * 2) 集中到共享模块后，runner 与生成核心都只依赖这里，后续若调整超时策略，不需要再跨文件搜索多个魔法数字。
 *
 * 关键边界条件与坑点：
 * 1) 这里只维护“洞府研修”链路，不能误复用到伙伴招募或云游奇遇；其他业务的耗时结构不同，必须各自评估。
 * 2) 超时值使用毫秒制，调用方不得再额外做秒转毫秒换算，避免出现 300 被误当成 300ms 或 300s 的单位错误。
 */
export const TECHNIQUE_GENERATION_TIMEOUT_MS = 300_000;
